
public class Calculadora {
	
	public double numero1;
	public double numero2;
	
	public double soma(){
		
		double result;
		result = numero1 + numero2;
		return result;
	}
	
	public double subtracao(){
		
		double result;
		result = numero1 - numero2;
		return result;
	}

	public double multiplicacao(){
	
	double result;
	result = numero1 * numero2;
	return result;
	}
	
	public double divisao(){
		
	double result;
	result = numero1 / numero2;
	return result;
	}	

	
}
